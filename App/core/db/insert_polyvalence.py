from configbd import get_db_connection

def insert_polyvalence_data(data):
    """
    Automatiser l'insertion des données dans la table polyvalence.
    
    :param data: Liste de tuples (operateur_id, poste_id, niveau)
    """
    try:
        # Connexion à la base de données
        connection = get_db_connection()
        cursor = connection.cursor()

        # Préparation de la requête d'insertion
        insert_query = """
        INSERT INTO polyvalence (operateur_id, poste_id, niveau)
        VALUES (%s, %s, %s)
        ON DUPLICATE KEY UPDATE niveau = VALUES(niveau)
        """  # Utilise ON DUPLICATE KEY UPDATE pour éviter les doublons

        # Exécution des données
        cursor.executemany(insert_query, data)
        connection.commit()

        print(f"{cursor.rowcount} lignes insérées ou mises à jour.")
    except Exception as e:
        print(f"Erreur lors de l'insertion des données : {e}")
    finally:
        # Fermeture de la connexion
        cursor.close()
        connection.close()

